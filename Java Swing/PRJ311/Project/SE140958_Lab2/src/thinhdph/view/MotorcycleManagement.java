    public void loadBrand() {
        try {
            BrandDAO brDAO = new BrandDAO();
            ArrayList<BrandDTO> listBrand = brDAO.loadBrand();//load 
            for(BrandDTO brDTO : listBrand) {
                Vector v = new Vector();
                v.add(brDTO.getBrandID());
                v.add(brDTO.getBrandName());
                v.add(brDTO.getBrandCountry());
                v.add(brDTO.getBrandDesciption());
                dataBrand.add(v);
            }
            tblBrand.updateUI();
            System.out.println(listBrand.size());
        } catch (Exception e) {
        }
    }
    
    public void loadBike() {
        try {
            BikeDAO bkDAO = new BikeDAO();
            ArrayList<BikeDTO> listBike = bkDAO.loadBike();
            for(BikeDTO bikeDTO : listBike) {
                Vector v = new Vector();
                v.add(bikeDTO.getMotocycleID());
                v.add(bikeDTO.getModel());
                v.add(bikeDTO.getYear());
                v.add(bikeDTO.getCondition());
                v.add(bikeDTO.getPrice());
                v.add(bikeDTO.getQuantity());
                v.add(bikeDTO.getWarranty());
                v.add(bikeDTO.getBrandID());
                dataBike.add(v);
            }
            //load combo box
            cbBrandName.removeAllItems();//make cb become null
            BrandDAO brDAO = new BrandDAO(); //call DAO 
            ArrayList<String> listBrandName = brDAO.getBrandNameForCbbox(); //load brandName in tblBIke into Arraylist
            for(String items : listBrandName) {//load each brandID into cbox
                cbBrandName.addItem(items);
            }
            
        } catch (Exception e) {
        }
    }

