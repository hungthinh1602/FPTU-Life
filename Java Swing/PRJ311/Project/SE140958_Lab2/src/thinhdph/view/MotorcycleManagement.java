    private void btnBrandSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrandSaveActionPerformed
  
        BrandDAO brDAO = new BrandDAO();

        boolean duplicate = true;
        StringBuilder sb = new StringBuilder();
        sb.append(checkInput.checkID(txtBrandID.getText().trim()));
        sb.append(checkInput.checkName(txtBrandName.getText().trim()));
        sb.append(checkInput.checkCountry(txtBrandCountry.getText().trim()));
        sb.append(checkInput.checkDecription(txtBrandDes.getText().trim()));
        

        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Notify", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        BrandDTO brDTO = new BrandDTO(txtBrandID.getText(), txtBrandName.getText(),
                                      txtBrandCountry.getText(), txtBrandDes.getText());
        
        if (addNewBrand == true) {
            try {
                duplicate = brDAO.checkDuplicate(txtBrandID.getText().trim());
                if (duplicate == false) {
                    brDAO.insertNewBrand(brDTO);
                    JOptionPane.showMessageDialog(this, "The brand " + txtBrandID.getText().trim() + " have been added!  ");
                    brandModel.setRowCount(0);
                    loadBrand();
                    loadBike();//load cbox
                    addNewBrand = false;
                } else {
                    JOptionPane.showMessageDialog(this, "This brand have been existed");
                }

            } catch (SQLException ex) {} 
              catch (ClassNotFoundException ex) {}
        } else {
            try {
                int choice = JOptionPane.showConfirmDialog(this, "Do you want to update brand " + txtBrandID.getText().trim() + " ?", "Notify", JOptionPane.YES_NO_OPTION);
                if (choice == JOptionPane.YES_OPTION) {
                    brDAO.updateBrand(brDTO);
                    JOptionPane.showMessageDialog(this, "Brand ID " + txtBrandID.getText().trim() + " have been updated!");
                    brandModel.setRowCount(0);
                    loadBrand();
                    loadBike();//load cbox
                }

            } catch (SQLException ex) {
                ex.printStackTrace();
            } catch (ClassNotFoundException ex) {
                ex.printStackTrace();
            }
        }

    }//GEN-LAST:event_btnBrandSaveActionPerformed

    private void btnBrandDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrandDeleteActionPerformed
        BrandDAO brDAO = new BrandDAO();
        String brandID = txtBrandID.getText().trim();
        try{
            int choice = JOptionPane.showConfirmDialog(this, "Do you want to detele brand " + txtBrandID.getText().trim() + " ?", "Notify", JOptionPane.YES_NO_OPTION);
            int numberOfBike = brDAO.count(brandID);
            if (choice == JOptionPane.YES_OPTION) {
                //count bike of tblBike by brandID
                if (numberOfBike <= 0) {//dont have any bike
                    brDAO.deteleBrand(txtBrandID.getText().trim());
                    JOptionPane.showMessageDialog(this, "Brand ID " + txtBrandID.getText().trim() + " have been deteled!");
                    brandModel.setRowCount(0);
                    loadBrand();
                } else //have 1 or more
                {
                    JOptionPane.showMessageDialog(this, "This brand have " + numberOfBike + " product. \nYou must delete all Motocycle with brand " + brandID + " to delete this brand!!!");
                }

            }
        } catch (SQLException ex) {ex.printStackTrace();} 
          catch (ClassNotFoundException ex) {ex.printStackTrace();}
    }//GEN-LAST:event_btnBrandDeleteActionPerformed

